package programsonjava;

public class TypeCasting {

	public static void main(String[] args) 
	{
		int x=10;
		float y=x;
		System.out.println(y);
		float x1=12;
		int y1=(int) x1;
		System.out.println(y1);
		char x2='A';
		int y2=x2;
		System.out.println(y2);
		String x3="reehana";
		String y3=x3;
		System.out.println(y3);
		

	}

}
